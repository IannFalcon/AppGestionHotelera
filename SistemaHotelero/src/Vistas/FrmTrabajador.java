    private void tablalistadoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablalistadoMouseClicked
        btnGuardar.setText("Editar");
        habilitar();
        btnEliminar.setEnabled(true);
        accion = "editar";

        int fila = tablalistado.rowAtPoint(evt.getPoint());

        txtIdPersona.setText(tablalistado.getValueAt(fila, 0).toString());
        txtNombre.setText(tablalistado.getValueAt(fila, 1).toString());
        txtApellido.setText(tablalistado.getValueAt(fila, 2).toString());
        cboTipoDocu.setSelectedItem(tablalistado.getValueAt(fila, 3).toString());
        txtNroDocu.setText(tablalistado.getValueAt(fila, 4).toString());
        txtTelefono.setText(tablalistado.getValueAt(fila, 5).toString());
        txtEmail.setText(tablalistado.getValueAt(fila, 6).toString());
        cboAcceso.setSelectedItem(tablalistado.getValueAt(fila, 7).toString());
        txtUsuario.setText(tablalistado.getValueAt(fila, 8).toString());
        txtPassword.setText(tablalistado.getValueAt(fila, 9).toString());
        cboEstado.setSelectedItem(tablalistado.getValueAt(fila, 10).toString());
    }//GEN-LAST:event_tablalistadoMouseClicked

