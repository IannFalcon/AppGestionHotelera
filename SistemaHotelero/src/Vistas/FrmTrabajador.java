    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        if(txtNombre.getText().length() == 0){
            JOptionPane.showConfirmDialog(rootPane, "Debes ingresar el nombre del empleado");
            txtNombre.requestFocus();
            return;
        }

        if(txtApellido.getText().length() == 0){
            JOptionPane.showConfirmDialog(rootPane, "Debes ingresar los apellidos del empleado");
            txtApellido.requestFocus();
            return;
        }
        
        if(txtNroDocu.getText().length() == 0){
            JOptionPane.showConfirmDialog(rootPane, "Debes ingresar el Nro.Documento del empleado");
            txtNroDocu.requestFocus();
            return;
        }
        
        if(txtTelefono.getText().length() == 0){
            JOptionPane.showConfirmDialog(rootPane, "Debes ingresar el Nro.Telefono del empleado");
            txtTelefono.requestFocus();
            return;
        }
        
        if(txtEmail.getText().length() == 0){
            JOptionPane.showConfirmDialog(rootPane, "Debes ingresar el correo del empleado");
            txtEmail.requestFocus();
            return;
        }

        if(txtUsuario.getText().length() == 0){
            JOptionPane.showConfirmDialog(rootPane, "Debes ingresar un usuario para el empleado");
            txtUsuario.requestFocus();
            return;
        }
        
        if(txtPassword.getText().length() == 0){
            JOptionPane.showConfirmDialog(rootPane, "Debes ingresar una contraseña para el empleado");
            txtPassword.requestFocus();
            return;
        }

        Trabajador tbl = new Trabajador();
        TrabajadorDAO func = new TrabajadorDAO();

        tbl.setNombre(txtNombre.getText());
        tbl.setApellido(txtApellido.getText());
        
        int seleccionado = cboTipoDocu.getSelectedIndex();
        tbl.setTipodocumento((String)cboTipoDocu.getItemAt(seleccionado));
        
        tbl.setNumdocumento(txtNroDocu.getText());
        tbl.setTelefono(txtTelefono.getText());
        tbl.setEmail(txtEmail.getText());
        
        seleccionado = cboAcceso.getSelectedIndex();
        tbl.setAcceso((String)cboAcceso.getItemAt(seleccionado));
        
        tbl.setUsuario(txtUsuario.getText());
        tbl.setClave(txtPassword.getText());
        
        seleccionado = cboEstado.getSelectedIndex();
        tbl.setEstado((String)cboEstado.getItemAt(seleccionado));

        if(accion.equals("guardar")){
            if(func.insertar(tbl)){
                JOptionPane.showConfirmDialog(rootPane, "El empleado fue registrado con exito");
                mostrar("");
                inhabilitar();
            }
        }else if(accion.equals("editar")){
            tbl.setIdpersona(Integer.parseInt(txtIdPersona.getText()));
            if(func.editar(tbl)){
                JOptionPane.showConfirmDialog(rootPane, "Los datos del empleado fueron actualizados con exito");
                mostrar("");
                inhabilitar();
            }
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

