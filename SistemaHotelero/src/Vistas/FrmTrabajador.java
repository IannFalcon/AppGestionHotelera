    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        if(!txtIdPersona.getText().equals("")){
            int confirmacion = JOptionPane.showConfirmDialog(rootPane,"¿Estas seguro de eliminar a este empleado?","Confirmar",2);
            if(confirmacion == 0){
                TrabajadorDAO func = new TrabajadorDAO();
                Trabajador tbl = new Trabajador();

                tbl.setIdpersona(Integer.parseInt(txtIdPersona.getText()));
                func.eliminar(tbl);
                mostrar("");
                inhabilitar();
            }
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

