    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        if(txtIdHabitacion.getText().length() == 0){
            JOptionPane.showConfirmDialog(rootPane, "Debes seleccionar una habitación");
            txtIdHabitacion.requestFocus();
            return;
        }

        if(txtIdCliente.getText().length() == 0){
            JOptionPane.showConfirmDialog(rootPane, "Debes seleccionar un cliente");
            txtIdCliente.requestFocus();
            return;
        }

        if(txtCosto.getText().length() == 0){
            JOptionPane.showConfirmDialog(rootPane, "Debes ingresar un precio para el alojamiento");
            txtCosto.requestFocus();
            return;
        }

        Reserva tbl = new Reserva();
        ReservaDAO func = new ReservaDAO();

        tbl.setIdhabitacion(Integer.parseInt(txtIdHabitacion.getText()));
        tbl.setIdcliente(Integer.parseInt(txtIdCliente.getText()));
        tbl.setIdtrabajador(idusuario);
        
        int seleccionado = cboTipoReserva.getSelectedIndex();
        tbl.setTiporeserva((String) cboTipoReserva.getItemAt(seleccionado));
        
        Calendar cal;
        int d,m,a;
        
        cal = dcFechaReserva.getCalendar();
        d = cal.get(Calendar.DAY_OF_MONTH);
        m = cal.get(Calendar.MONTH);
        a = cal.get(Calendar.YEAR) - 1900;
        tbl .setFechareserva(new Date(a,m,d));
        
        cal = dcFechaIngreso.getCalendar();
        d = cal.get(Calendar.DAY_OF_MONTH);
        m = cal.get(Calendar.MONTH);
        a = cal.get(Calendar.YEAR) - 1900;
        tbl .setFechaingreso(new Date(a,m,d));
        
        cal = dcFechaSalida.getCalendar();
        d = cal.get(Calendar.DAY_OF_MONTH);
        m = cal.get(Calendar.MONTH);
        a = cal.get(Calendar.YEAR) - 1900;
        tbl .setFechasalida(new Date(a,m,d));
        
        tbl.setCostoalojamiento(Double.parseDouble(txtCosto.getText()));
        
        seleccionado = cboEstado.getSelectedIndex();
        tbl.setEstado((String) cboEstado.getItemAt(seleccionado));

        if(accion.equals("guardar")){
            if(func.insertar(tbl)){
                JOptionPane.showConfirmDialog(rootPane, "La reserva fue registrada satisfactoriamente");
                mostrar("");
                
                HabitacionDAO func2 = new HabitacionDAO();
                Habitacion tbl2 = new Habitacion();
                
                tbl2.setIdhabitacion(Integer.parseInt(txtIdHabitacion.getText()));
                func2.ocupar(tbl2);
                
                inhabilitar();
            }
        }else if(accion.equals("editar")){
            tbl.setIdreserva(Integer.parseInt(txtIdReserva.getText()));
            tbl.setIdtrabajador(Integer.parseInt(txtIdTrabajador.getText()));
            if(func.editar(tbl)){
                JOptionPane.showConfirmDialog(rootPane, "La reserva fue modificada satisfactoriamente");
                mostrar("");
                inhabilitar();
            }
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

