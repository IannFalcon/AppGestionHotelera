    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        if(!txtIdReserva.getText().equals("")){
            int confirmacion = JOptionPane.showConfirmDialog(rootPane,"¿Estas seguro de eliminar esta reserva?","Confirmar",2);
            if(confirmacion == 0){
                ReservaDAO func = new ReservaDAO();
                Reserva tbl = new Reserva();

                tbl.setIdreserva(Integer.parseInt(txtIdReserva.getText()));
                func.eliminar(tbl);
                mostrar("");
                inhabilitar();
            }
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

